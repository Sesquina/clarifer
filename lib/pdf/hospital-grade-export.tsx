function EmergencyBlock({ data }: { data: ExportData }) {
  const { patient } = data;
  if (!patient.emergency_card_enabled) return null;
  const allergies = patient.allergies && patient.allergies.length > 0 ? patient.allergies.join(", ") : null;
  return (
    <View>
      <Text style={styles.sectionHeader}>Emergency information</Text>
      <View style={styles.emergencyBox}>
        {patient.blood_type && <Text style={styles.body}>Blood type: {patient.blood_type}</Text>}
        {allergies && <Text style={styles.body}>Allergies: {allergies}</Text>}
        {patient.emergency_contact_name && (
          <Text style={styles.body}>
            Emergency contact: {patient.emergency_contact_name}
            {patient.emergency_contact_phone ? ` -- ${patient.emergency_contact_phone}` : ""}
          </Text>
        )}
        {patient.dpd_deficiency_status && (
          <Text style={styles.badgeAccent}>DPD deficiency status: {patient.dpd_deficiency_status}</Text>
        )}
      </View>
    </View>
  );
}

