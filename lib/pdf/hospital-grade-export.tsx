/**
 * Builds the @react-pdf Document tree. Single Page with `wrap` so the
 * fixed footer renders on every page; @react-pdf paginates content
 * automatically when sections overflow the LETTER size.
 */
export function buildHospitalGradeDocument(data: ExportData) {
  return (
    <Document
      title={`Clarifer Medical Summary -- ${data.patient.name ?? "Patient"}`}
      author={data.generatedBy || "Clarifer"}
      subject={data.patient.name ?? "Patient"}
      producer="Clarifer"
    >
      <Page size="LETTER" style={styles.page} wrap>
        <HeaderBlock data={data} />
        <DisclaimerBlock />
        <EmergencyBlock data={data} />
        <MedicationsBlock data={data} />
        <SymptomsBlock data={data} />
        <AppointmentsBlock data={data} />
        <DocumentsBlock data={data} />
        <CareTeamBlock data={data} />
        <ProviderNotesBlock data={data} />
        <Footer generatedAt={data.generatedAt} />
      </Page>
    </Document>
  );
}

/**
 * Renders the document to a binary PDF buffer. Caller is responsible
 * for streaming the bytes back without writing to disk.
 */
export async function renderHospitalGradePdf(data: ExportData): Promise<Uint8Array> {
  const doc = buildHospitalGradeDocument(data);
  const buffer = await renderToBuffer(doc);
  return buffer instanceof Uint8Array ? buffer : new Uint8Array(buffer);
}
